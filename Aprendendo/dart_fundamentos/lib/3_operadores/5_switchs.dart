void main() {

  final diaDaSemana = 10;

  switch (diaDaSemana) {
    case 0:
      print('Domingo');
      break;
    case 1:
      print('Segunda Feira');
      break;
    case 2:
      print('Terça Feira');
      break;
    case 3:
      print('Quarta Feira');
      break;
    case 4:
      print('Quinta Feira');
      break;
    case 5:
      print('Sexta Feira');
      break;
    case 6:
      print('Sabado');
      break;
    default:
      print('Não identificado');
  }
}